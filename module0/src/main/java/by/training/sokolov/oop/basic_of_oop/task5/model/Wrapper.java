package by.training.sokolov.oop.basic_of_oop.task5.model;

public class Wrapper {

    private String color;
    private String type;

    public Wrapper(String color, String type) {
        this.color = color;
        this.type = type;
    }


    public String getColor() {
        return color;
    }

    public String getType() {
        return type;
    }

}
